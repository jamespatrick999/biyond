const getBlockchain = () =>
  new Promise( async (resolve, reject) => {
    let provider = await detectEthereumProvider();
    if(provider) {
      const accounts = await provider.request({ method: 'eth_requestAccounts' });
      const currentAcc = accounts[0] ;
      provider = new ethers.providers.Web3Provider(provider);
      const signer = provider.getSigner();
      const tokenInstance = new Contract(
       tokenAddress,
       tokenABI,
       signer
      );
	  const usdtInstance = new Contract(
		usdtAddress,
		usdtABI,
		signer
	   );
	   const contractInstance = new Contract(
		contractAddress,
		contractABI,
		signer
	   );
      resolve({tokenInstance, currentAcc, usdtInstance, contractInstance});
      return;
    }
    reject('Install/Use Web3 provider like Metamask or Trust Wallet ');
  });

export default getBlockchain;
